public class ST
{
    public static string 建造叠加
    {
        get
        {
            if (Localization.language == Language.zhCN)
            {
                return "建造叠加";
            }
            else
            {
                return "AddBuild";
            }
        }
    }

    public static string 速度
    {
        get
        {
            if (Localization.language == Language.zhCN)
            {
                return "速度";
            }
            else
            {
                return "Speed";
            }
        }
    }
}